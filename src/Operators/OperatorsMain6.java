package Operators;

public class OperatorsMain6 {
    public static void main(String[] arg){
        int a = 10;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a++ + a++);
        System.out.println(++a + ++a);
    }
}
